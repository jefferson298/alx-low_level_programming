#include "main.h"
/**
 * _memcpy - function that copies memory area
 * @dest:The memory Storage area
 * @src: The Memory source
 * *@n: The Total number of bytes
 *
 * Return: a pointer to dest
 */

char *_memcpy(char *dest, char *src, unsigned int n)
{
	int g = 0;
	int h = n;

	for (; g < h; g++)
	{
		dest[g] = src[g];
		n--;
	}
	return (dest);
}
